     public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) jTProfessor.getModel();
        
        ProfessorDao profDao = new ProfessorDao();
        
        for(Professor pro: profDao.read()){
            modelo.addRow(new Object[]{
                pro.getId(),
                pro.getNome(),
                pro.getEmail(),
                pro.getCpf(),
                pro.getRg(),
                pro.getEstado(),
                pro.getCampus(),
                pro.getCidade(),
                pro.getRua(),
                pro.getNumCasa(),
                pro.getTelefone()
            });    

        }
    }
    
     public void limpajTable(){
        DefaultTableModel modelo = (DefaultTableModel) jTProfessor.getModel();
        while (jTProfessor.getModel().getRowCount() > 0)
            modelo.removeRow(0);
    }
    
    public void selecionaLinha(){ //Seleciona a linha da tabela
         if(jTProfessor.getSelectedRow() != -1){ 
            nome.setText(jTProfessor.getValueAt(jTProfessor.getSelectedRow(), 1).toString()); 
            
            
            email.setText(jTProfessor.getValueAt(jTProfessor.getSelectedRow(), 2).toString()); 
            cpf.setText(jTProfessor.getValueAt(jTProfessor.getSelectedRow(), 3).toString()); 
            rg.setText(jTProfessor.getValueAt(jTProfessor.getSelectedRow(),4).toString());

            for(Estado est: new EstadoDao().read())
                if(est.getUf().equals(jTProfessor.getValueAt(jTProfessor.getSelectedRow(), 5).toString())){
                    estado.getModel().setSelectedItem(est);
                }

            for(Campus camp: new CampusDao().read()){
                if(camp.getNome().equals(jTProfessor.getValueAt(jTProfessor.getSelectedRow(),6).toString())){
                    campus.getModel().setSelectedItem(camp);
                }
            }
            

            cidade.setText(jTProfessor.getValueAt(jTProfessor.getSelectedRow(), 7).toString());
            rua.setText(jTProfessor.getValueAt(jTProfessor.getSelectedRow(),8).toString());
            numCasa.setText(jTProfessor.getValueAt(jTProfessor.getSelectedRow(), 9).toString());
            telefone.setText(jTProfessor.getValueAt(jTProfessor.getSelectedRow(), 10).toString());
       }
       else{
           JOptionPane.showMessageDialog(null,"Selecione uma linha");
       }
     }

