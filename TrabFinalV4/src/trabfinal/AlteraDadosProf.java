    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        Campus camp = (Campus) campus.getSelectedItem();//faz um cast de acordo com o item selecionado
        Estado estad = (Estado) estado.getSelectedItem();
        
        ProfessorDao pd = new ProfessorDao();
        Professor prof = new Professor();

        if(jTProfessor.getSelectedRow() != -1){
            prof.setId(jTProfessor.getValueAt(jTProfessor.getSelectedRow(), 0).toString());
            prof.setNome(nome.getText());
            prof.setCpf(cpf.getText());
            prof.setRg(rg.getText());
            prof.setRua(rua.getText());
            prof.setCidade(cidade.getText());
            prof.setNumCasa(numCasa.getText());
            prof.setTelefone(telefone.getText());
            prof.setEmail(email.getText());
            prof.setIdCampus(camp.getIdCampus());
            prof.setIdEstado(estad.getId());
            

            pd.update(prof);
            limpajTable();
            readJTable();

            nome.setText("");
            cpf.setText("");
            rg.setText("");
            rua.setText("");
            cidade.setText("");
            numCasa.setText("");
            telefone.setText("");
            email.setText("");
        }else{
            JOptionPane.showMessageDialog(null,"Selecione uma linha");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

