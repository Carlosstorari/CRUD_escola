      public void limpajTable(){
        DefaultTableModel modelo = (DefaultTableModel) jTBoletim.getModel();
        while (jTBoletim.getModel().getRowCount() > 0)
            modelo.removeRow(0);
    }
    
    public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) jTBoletim.getModel();
        
        BoletimDao boleDao = new BoletimDao();
        
        for(Boletim bole: boleDao.read2()){
            modelo.addRow(new Object[]{
                bole.getIdBoletim(),
                bole.getMedia(),
                bole.getFalta(),
                bole.getNomeAluno(),
                bole.getMateria()
            });    

        }
    }
    
    public void selecionaLinha(){ //Seleciona a linha da tabela
         if(jTBoletim.getSelectedRow() != -1){ 
            media.setText(jTBoletim.getValueAt(jTBoletim.getSelectedRow(), 1).toString()); 
            faltas.setText(jTBoletim.getValueAt(jTBoletim.getSelectedRow(), 2).toString());
       }
       else{
           JOptionPane.showMessageDialog(null,"Selecione uma linha");
       }
     }

