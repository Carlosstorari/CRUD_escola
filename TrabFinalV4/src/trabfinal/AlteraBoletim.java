    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        BoletimDao bd = new BoletimDao();
        Boletim bole = new Boletim();

        if(jTBoletim.getSelectedRow() != -1){
            bole.setIdBoletim(Integer.parseInt(jTBoletim.getValueAt(jTBoletim.getSelectedRow(), 0).toString()));
            bole.setFalta(Integer.parseInt(faltas.getText()));
            bole.setMedia(Float.parseFloat(media.getText()));
            
            bd.update(bole);
            limpajTable();
            readJTable();

            faltas.setText("");
            media.setText("");
            
        }else{
            JOptionPane.showMessageDialog(null,"Selecione uma linha");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

