    public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) jTProfessor.getModel();
        
        ProfessorDao profDao = new ProfessorDao();
        
        for(Professor prof: profDao.read()){
            modelo.addRow(new Object[]{
                prof.getId(),
                prof.getNome(),
                prof.getEmail(),
                prof.getCpf(),
                prof.getRg(),
                prof.getCampus(),
                prof.getEstado(),
                prof.getCidade(),
                prof.getRua(),
                prof.getNumCasa(),
                prof.getTelefone(),
                new SimpleDateFormat("dd/MM/YY").format(prof.getDataNascimento().getTime())
            });    

        }
    }
    

