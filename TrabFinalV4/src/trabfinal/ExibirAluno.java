    public void readJTable(){
        DefaultTableModel modelo = (DefaultTableModel) jTAluno.getModel();
        
        AlunoDao aluDao = new AlunoDao();
        
        for(Alunos alu: aluDao.read()){
            modelo.addRow(new Object[]{
                alu.getId(),
                alu.getNome(),
                alu.getCurso(),
                alu.getEmail(),
                alu.getCpf(),
                alu.getRg(),
                alu.getEstado(),
                alu.getCampus(),
                alu.getCidade(),
                alu.getRua(),
                alu.getNumCasa(),
                alu.getTelefone(),
                new SimpleDateFormat("dd/MM/YY").format(alu.getDataNascimento().getTime())
            });    

        }
    }

